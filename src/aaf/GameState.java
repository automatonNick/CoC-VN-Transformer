    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String searcher = skillsearch.getText().trim();
         Random rand= new Random();
        if(pc==null){
            try{
                throw new NoCharacterException();
            } catch(NoCharacterException e){
                JOptionPane.showMessageDialog(this, "Please select your character");
            }
        }
        else{
            
        if(searcher.equalsIgnoreCase("bear") || searcher.equalsIgnoreCase("villager")){

            if(pc.compare(pc, DB_Access.PC_SkillSearch(searcher))==-1){
                jLabel74.setText("Enemy goes first");
            }
            else if(pc.compare(pc, DB_Access.PC_SkillSearch(searcher))==1){
                jLabel74.setText("You go first");
            }
        }else if(searcher.equalsIgnoreCase("bear fighting") || searcher.equalsIgnoreCase("villager fighting")){
            String[] st=searcher.split(" ");
            Character cr = DB_Access.PC_SkillSearch(st[0]);          
            
            if(cr.compare(pc, DB_Access.PC_SkillSearch(st[0].trim(), st[1].trim()))==-1){
                jLabel74.setText("Your attack hits");
            }
            else if(cr.compare(pc, DB_Access.PC_SkillSearch(st[0].trim(), st[1].trim()))==1){
                jLabel74.setText("Your attack misses");
            }
        }
        else{
            int skill = DB_Access.PC_SkillSearch(searcher, pc);

        int diethrow = rand.nextInt(99)+1;
        jLabel73.setText(String.valueOf(diethrow));
        if(diethrow<skill) jLabel74.setText("Success");
        else jLabel74.setText("Failure");
        }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

